class Node:
    def __init__(self, data):
        self.left = None
        self.right = None
        self.data = data

    def insert(self, data):
        if self.data:
            if data < self.data:
                if self.left is None:
                    self.left = Node(data)
                else:
                    self.left.insert(data)
            elif data > self.data:
                if self.right is None:
                    self.right = Node(data)
                else:
                    self.right.insert(data)
        else:
            self.data = data

    def PrintTree(self):
        if self.left:
            self.left.PrintTree()
        print(self.data)
        if self.right:
            self.right.PrintTree()





class Node:
    def __init__(self, coord):
        self.left = None
        self.right = None
        self.data = coord

    def PrintTree(self):
        if self.left:
            self.left.PrintTree()
        print(self.data)
        if self.right:
            self.right.PrintTree()

def buildTree(coords):
    if not coords:
        return None
    mid = len(coords) // 2
    node = Node(coords[mid])
    node.left = buildTree(coords[:mid])
    node.right = buildTree(coords[mid+1:])
    return node

# Create 64 unique (x, y) coordinates (8x8 grid)
coords = [(x, y) for x in range(8) for y in range(8)]

# Sort coordinates (first by x, then y)
coords.sort()


root = buildTree(coords)

# Print the tree (in-order)
root.PrintTree()



